package com.example.rin.weatherapppart2.Model;

public class ToaDo {
    String kinhDo;
    String viDo;

    public ToaDo() {
    }

    public ToaDo(String kinhDo, String viDo) {
        this.kinhDo = kinhDo;
        this.viDo = viDo;
    }

    public String getKinhDo() {
        return kinhDo;
    }

    public void setKinhDo(String kinhDo) {
        this.kinhDo = kinhDo;
    }

    public String getViDo() {
        return viDo;
    }

    public void setViDo(String viDo) {
        this.viDo = viDo;
    }
}
